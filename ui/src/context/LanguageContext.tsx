const LanguageContext = createContext<LanguageContextValue | undefined>(undefined);

/**
 * 从本地存储读取语言设置
 * @returns 语言代码
 */
function readLanguageFromStorage(): Language {
  if (typeof window === "undefined") return "en";
  try {
    const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY) as Language;
    return stored === "zh" || stored === "en" ? stored : "en";
  } catch {
    return "en";
  }
}

/**
 * 翻译函数
 * @param language 当前语言
 * @param key 翻译键
 * @param vars 插值变量
 * @returns 翻译后的文本
 */
function translate(language: Language, key: string, vars?: Record<string, string>): string {
  let text = translations[language][key] || key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replace(new RegExp(`{${k}}`, "g"), v);
    });
  }
  return text;
}

/**
 * 语言提供者组件
 * @param children 子组件
 */
export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => readLanguageFromStorage());

  /**
   * 设置语言
   * @param nextLanguage 目标语言
   */
  const setLanguage = useCallback((nextLanguage: Language) => {
    setLanguageState(nextLanguage);
  }, []);

  /**
   * 切换语言
   */
  const toggleLanguage = useCallback(() => {
    setLanguageState((current) => (current === "zh" ? "en" : "zh"));
  }, []);

  /**
   * 翻译函数
   * @param key 翻译键
   * @param vars 插值变量
   * @returns 翻译后的文本
   */
  const t = useCallback(
    (key: string, vars?: Record<string, string>) => translate(language, key, vars),
    [language]
  );

  /**
   * 格式化日期
   * @param date 日期
   * @returns 格式化后的日期字符串
   */
  const formatDate = useCallback(
    (date: Date | string) => {
      const d = new Date(date);
      if (language === "zh") {
        return `${d.getFullYear()}年${d.getMonth() + 1}月${d.getDate()}日`;
      }
      return d.toLocaleDateString("en-US", {
        month: "short",
        day: "numeric",
        year: "numeric",
      });
    },
    [language]
  );

  /**
   * 格式化日期时间
   * @param date 日期
   * @returns 格式化后的日期时间字符串
   */
  const formatDateTime = useCallback(
    (date: Date | string) => {
      const d = new Date(date);
      if (language === "zh") {
        return `${d.getFullYear()}年${d.getMonth() + 1}月${d.getDate()}日 ${d.getHours()}:${d.getMinutes().toString().padStart(2, "0")}`;
      }
      return d.toLocaleString("en-US", {
        month: "short",
        day: "numeric",
        year: "numeric",
        hour: "numeric",
        minute: "2-digit",
      });
    },
    [language]
  );

  /**
   * 相对时间
   * @param date 日期
   * @returns 相对时间字符串
   */
  const relativeTime = useCallback(
    (date: Date | string) => {
      const now = Date.now();
      const then = new Date(date).getTime();
      const diffSec = Math.round((now - then) / 1000);
      const isZh = language === "zh";
      if (diffSec < 60) return isZh ? "刚刚" : "just now";
      const diffMin = Math.round(diffSec / 60);
      if (diffMin < 60) return isZh ? `${diffMin}分钟前` : `${diffMin}m ago`;
      const diffHr = Math.round(diffMin / 60);
      if (diffHr < 24) return isZh ? `${diffHr}小时前` : `${diffHr}h ago`;
      const diffDay = Math.round(diffHr / 24);
      if (diffDay < 30) return isZh ? `${diffDay}天前` : `${diffDay}d ago`;
      return formatDate(date);
    },
    [language, formatDate]
  );

  // 持久化语言设置到本地存储
  useEffect(() => {
    try {
      localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    } catch {
      // 忽略受限环境下的存储失败
    }
  }, [language]);

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      toggleLanguage,
      t,
      formatDate,
      formatDateTime,
      relativeTime,
    }),
    [language, setLanguage, toggleLanguage, t, formatDate, formatDateTime, relativeTime]
  );

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

/**
 * 使用语言上下文的Hook
 * @returns 语言上下文值
 * @throws 如果不在LanguageProvider内会抛出错误
 */
export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within LanguageProvider");
  }
  return context;
}
